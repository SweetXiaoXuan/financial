package cn.com.ql.wiseBeijing.auth.frontBean;

public class FVersion {
	private String version;
	private String limited_version;
	private String pubdate;
	private String content;
	private String url;
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getLimited_version() {
		return limited_version;
	}
	public void setLimited_version(String limited_version) {
		this.limited_version = limited_version;
	}
	public String getPubdate() {
		return pubdate;
	}
	public void setPubdate(String pubdate) {
		this.pubdate = pubdate;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
}
